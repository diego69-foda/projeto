public class Aluno {
    String nome;
    int idade;
    String curso;
    double notaMedia;

    // Construtor que usa a classe Teclado
   public Aluno(String nome, int idade, String curso, double notaMedia) {
        this.nome = nome;
        this.idade = idade;
        this.curso = curso;
        this.notaMedia = notaMedia;
    }
}
